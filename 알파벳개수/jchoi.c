#include <stdio.h>
int	main(void)
{
	char	S[128];
	int		count[26] = {0, };
	int		i;

	scanf("%s", S);
	i = 0;
	while (S[i])
	{
		count[S[i] - 'a']++;
		i++;
	}
	i = 0;
	while (i < 26)
	{
		printf("%d ", count[i]);
		i++;
	}
	return (0);
}
