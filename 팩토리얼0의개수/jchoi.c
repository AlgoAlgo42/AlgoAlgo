#include <stdio.h>
int    main(void)
{
    int n, i, zeroes = 0;
    
    scanf("%d", &n);
    while(0 < n)
    {
        i = n;
        while (0 < i)
        {
            if (i % 5)
                break;
            zeroes++;
            i /= 5;
        }
        n--;
    }
    printf("%d",zeroes);
    return (0);
}
